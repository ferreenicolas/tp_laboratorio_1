float suma(float n1, float n2)
{
float resultado;
resultado = n1 + n2;
return resultado;
}

float resta(float n1, float n2)
{
float resultado;
resultado = n1 - n2;
return resultado;
}

float multiplicacion(float n1, float n2)
{
float resultado;
resultado = n1 * n2;
return resultado;
}

float division(float n1, float n2)
{
float resultado;
resultado = n1 / n2;
return resultado;
}

